package beans;

public class DistritoBean {
    private String ID;
    private String ID_PROVINCIA;
    private String NOM_DISTRITO;

    public DistritoBean() {
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getID_PROVINCIA() {
        return ID_PROVINCIA;
    }

    public void setID_PROVINCIA(String ID_PROVINCIA) {
        this.ID_PROVINCIA = ID_PROVINCIA;
    }

    public String getNOM_DISTRITO() {
        return NOM_DISTRITO;
    }

    public void setNOM_DISTRITO(String NOM_DISTRITO) {
        this.NOM_DISTRITO = NOM_DISTRITO;
    }
}
